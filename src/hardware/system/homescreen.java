    private void jTree1ValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTree1ValueChanged
        // TODO add your handling code here:
    String node = evt.getNewLeadSelectionPath().getLastPathComponent().toString();
    if( node.equals("New Items") ) {
         newItem open = new newItem();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
    } else if( node.equals("Update Items") ) {
        UpdateItems open = new UpdateItems();
        open.setVisible(true);
        open.setLocationRelativeTo(null);}
    else if(node.equals("Query")){
        Query open = new Query();
        open.setVisible(true);
        open.setLocationRelativeTo(null);}
    else if(node.equals("Restocking")){
        restocking open = new restocking();
        open.setVisible(true);
        open.setLocationRelativeTo(null);}
      else if(node.equals("Credit Items")){
        CreditItems open = new CreditItems();
        open.setVisible(true);
        open.setLocationRelativeTo(null);}
     else if(node.equals("New Debtors")){
        newDebtor open = new newDebtor();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
      } else if(node.equals("Current Debtors")){
        viewDebtor open = new viewDebtor();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
      }else if(node.equals("Sale")){
        sales open = new sales();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
      }else if(node.equals("Cancel Receipt")){
        CancelTransaction open = new CancelTransaction();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
      }else if(node.equals("Quotation")){
        quotation open = new quotation();
        open.setVisible(true);
        open.setLocationRelativeTo(null);
      }
    }//GEN-LAST:event_jTree1ValueChanged

