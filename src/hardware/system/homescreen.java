public void getNotifications(){
String sql = "Select `Name`,`Quantity`,`restock` From `inventory`";
try{
 pst = conn.prepareStatement(sql);
 rs = pst.executeQuery();
 while(rs.next())
 {
no.setText(rs.getString("restock"));
quantity.setText(rs.getString("Quantity"));
name.setText(rs.getString("Name"));
int a = Integer.parseInt(no.getText());
int b = Integer.parseInt(quantity.getText());
if(a>=b){
notifications.setForeground(Color.blue);
notifications.add(name.getText()+ " needs to be restocked");
}
 }
}catch(Exception e){
JOptionPane.showMessageDialog(null, e);
}
}
